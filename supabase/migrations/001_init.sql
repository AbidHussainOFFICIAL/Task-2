CREATE INDEX IF NOT EXISTS idx_quotations_vendor_id
    ON quotations(vendor_id);

CREATE INDEX IF NOT EXISTS idx_quotations_title
    ON quotations(quotation_title);

CREATE INDEX IF NOT EXISTS idx_quotations_status
    ON quotations(status);

CREATE INDEX IF NOT EXISTS idx_quotations_amount
    ON quotations(quotation_amount);

CREATE INDEX IF NOT EXISTS idx_quotations_submission_date
